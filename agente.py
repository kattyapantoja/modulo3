def load_pdf_as_documents(filepath, titulo, categoria, origen, idioma):
    pdf = fitz.open(filepath)
    docs = []
    for i, page in enumerate(pdf):
        text = page.get_text()
        if text.strip():
            docs.append(Document(
                page_content=text,
                metadata={
                    "page": i + 1,
                    "categoria": categoria,
                    "idioma": "es",
                    "origen": origen,
                    "titulo": titulo
                }
            ))
    return docs

